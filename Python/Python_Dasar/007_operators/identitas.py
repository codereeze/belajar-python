# operator identitas digunakan untuk memeriksa apakah kedua variable memiliki identitas yang sama

#** <=================================================================> **#
#** <=================================================================> **#


# operator identitas is
a = 10
b = 10
c = a is b # apakah variable a memiliki identitas seperti variable b

print(c)


# operator identitas is not
d = 20
e = 18
f = d is not e # apakah variable d tidak memiliki identitas yang sama seperti variable e

print(f)