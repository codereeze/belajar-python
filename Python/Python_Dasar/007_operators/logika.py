# operator logika adalah operator yang digunakan untuk melakukan operasi logika
# seperti dan (and), atau (or), dan tidak (not)

#** <=================================================================> **#
#** <=================================================================> **#


# operator and
# operator ini akan menghasilkan nilai true jika kedua operandnya bernilai benar
operator_and = True and True
print(operator_and)


# operator or
# operator ini akan menghasilkan nilat true jika salah satunya bernilai true
operator_or = True or False
print(operator_or)


# operator not
# operator ini digunakan untuk membalikan nilai. misalkan false jadi true
operator_not = not False
print(operator_not)