# operator perbandingan adalah operator yang digunakan untuk membandingkan dua buah nilai
# yang nantinya hasil perbandingan itu menghasilkan nilai boolean

#** <=================================================================> **#
#** <=================================================================> **#

# lebih besar dari
# akan true jika nilai yang dikiri lebih besar dari yang kanan
lebih_besar_dari = 10 > 5
print("10 lebih besar dari 5 hasilnya", lebih_besar_dari)


# lebih kecil dari
# akan true jika nilai yang dikiri lebih kecil dari yang kanan
lebih_kecil_dari = 5 < 10
print("5 lebih kecil dari 10 hasilnya", lebih_kecil_dari)


# lebih besar sama dengan
# akan true jika nilai yang dikiri lebih besar atau sama dengan yang kanan
lebih_besar_sama_dengan = 10 >= 10
print("10 lebih besar sama dengan 10 hasilnya", lebih_besar_sama_dengan)


# lebih kecil sama dengan
# akan true jika nilai yang dikiri lebih kecil atau sama dengan yang kanan
lebih_kecil_sama_dengan = 2 <= 2
print("2 lebih kecil sama dengan 2 hasilnya", lebih_kecil_sama_dengan)


# sama dengan
# akan true jika nilai yang dikiri sama dengan yang dikanan
sama_dengan = 10 == 10
print("10 sama dengan 10 hasilnya", sama_dengan)


# tidak sama dengan
# akan true jika nilai yang dikiri tidak sama dengan yang dikanan
tidak_sama_dengan = 10 != 5
print("10 tidak sama dengan 5 hasilnya", tidak_sama_dengan)