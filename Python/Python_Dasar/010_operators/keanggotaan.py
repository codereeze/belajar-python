# operator keanggotaan adalah operator yang digunakan untuk mengecek nilai dari variable apakah memiliki nilai tertentu

#** <=================================================================> **#
#** <=================================================================> **#

# operator keanggotaan in
name = "Atyla Azfa Al Harits"
check_value = "Atyla" in name # apakah nilai string Atyla ada di dalam variable name

print(check_value)




# operator keanggotaan not in
age = "18"
check_value = "17" not in age # apakah nilai integer 17 tidak ada di dalam variable age

print(check_value)