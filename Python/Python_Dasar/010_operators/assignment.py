# operator penugasan (assignment) adalah operator yang digunakan untuk mengisi atau memperbarui nilai di suatu variable

#** <=================================================================> **#
#** <=================================================================> **#


# operator penugasan pada variable
a = 10 # menugaskan variable a untuk menampung nilai 10


# operator penugasan untuk operasi matematika

# penjumlahan
a = 10
a += 5
print("Hasil penjumlahan", a)


# pengurangan
a = 20
a -= 10
print("Hasil pengurangan", a)


# perkalian
a = 10
a *= 10
print("Hasil perkalian", a)


# pembagian
a = 15
a /= 3
print("Hasil pembagian", a)


# pemangkatan
a = 20
a **= 5
print("Hasil pemangkatan", a)


# pembagian hasil bulat
a = 15
a //= 3
print("Hasil pembagian bulat", a)


# modulus
a = 5
a %= 2
print("Hasil modulus", a)