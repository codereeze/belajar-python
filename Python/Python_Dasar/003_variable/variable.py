# variable adalah tempat menampung data yang bersifat sementara atau bisa berubah-ubah nilainya (mutable)
# variable bisa digunakan menampung semua jenis tipe data
# untuk membuat variable kita hanya tinggal mengetikan nama variable nya saja

#** <=================================================================> **#
#** <=================================================================> **#

# contoh membuat variable

name = "Atyla Azfa Al Harits"
code_class = "19.1A.17"
city = "Tasikmalaya, Indonesia"

# cetak nilai variable
print(name)
print(code_class)
print(city)


# kita bisa mengubah nilai variable sebelumnya dengan nilai yang baru
name = "Guido van Rossum"

print(name)