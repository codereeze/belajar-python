# casting adalah proses memengubah (konversi) tipe data dari satu jenis tipe data ke tipe data yang lain 

#** <=================================================================> **#
#** <=================================================================> **#


# casting string
print("casting string")
age = str(17)
pi = str(3.14)

print(age)
print(pi)


print()


# casting integer
print("casting integer")
age = int("18")
pi = int(3.14)
boolean = int(True)

print(age)
print(pi)
print(boolean)


print()


# casting float
print("casting float")
age = float(10)
pi = float("3.14")

print(age)
print(pi)


print()


# casting boolean
print("casting boolean")
true = bool(1)
false = bool(0)

print(true)
print(false)